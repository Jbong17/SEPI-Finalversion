# PDF Generator Function Placeholder
def generate_pdf(data):
    filename = f"enrollment_{uuid4().hex}.pdf"
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", size=12)
    pdf.multi_cell(0, 10, safe_text("Enrollment Form - School of Everlasting Pearl, Inc"))
    for key, value in data.items():
        pdf.multi_cell(0, 8, safe_text(f"{key.replace('_', ' ').title()}: {value}"))
    pdf.output(filename)
    return filename

